#include "3-calc.h"
/**
 * op_add - sum of two variables
 * @a: var
 * @b: var
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sustraction
 * @a: var
 * @b: var
 *
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - product for two variables
 * @a: var
 * @b: var
 *
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Divide two numbers
 * @a: var
 * @b: var
 *
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod
 * @a: var
 * @b: var
 *
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
